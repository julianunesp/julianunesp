var = input('Type something: ')

print(f'Its type is: {type(var)}')
print(f'It has only spaces? {var.isspace()}')
print(f'Is it numeric? {var.isnumeric()}')
print(f'Is it alphanumeric? {var.isalpha()}')
print(f'Is it uppercased? {var.isupper()}')
print(f'Is it lowercased? {var.islower()}')
print(f'Is it capitalized? {var.istitle()}')
